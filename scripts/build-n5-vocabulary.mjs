const byKanji = new Map();
for (const [k, word, reading, meaning] of ROWS) {
  if (!byKanji.has(k)) byKanji.set(k, []);
  const list = byKanji.get(k);
  if (list.some((e) => e.word === word)) continue;
  list.push({ word, reading, meaning });
}

const lines = [
  "import type { VocabularySeed } from '../types';",
  '',
  '/** N5 vocabulary grouped by parent kanji character (display order = sort_order). */',
  'export const N5_VOCABULARY_BY_CHARACTER: Record<string, VocabularySeed[]> = {',
];

for (const [k, items] of [...byKanji.entries()].sort((a, b) => a[0].localeCompare(b[0], 'ja'))) {
  const top = items.slice(0, 5);
  lines.push(`  '${k}': [`);
  for (const v of top) {
    const w = v.word.replace(/'/g, "\\'");
    const m = v.meaning.replace(/'/g, "\\'");
    lines.push(`    { word: '${w}', reading: '${v.reading}', meaning: '${m}' },`);
  }
  lines.push('  ],');
}
lines.push('};');
lines.push('');

const outPath = path.join(__dirname, '../src/data/n5Vocabulary.ts');
fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log('Wrote', outPath, 'kanji groups:', byKanji.size);
